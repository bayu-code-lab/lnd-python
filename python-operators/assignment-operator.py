# deklarasi variable
a = 10
b = 1

# addition/penjumlahan
a += b
print(a)

# substraction/pengurangan
a -= b
print(a)

# divide/pembagian
a /= b
print(a)

# multiply/perkalian
a *= b
print(a)

# modulo/modulus
a %= b
print(a)

# exponent/pangkat
a **= b
print(a)

# floor division/sisa pembagian
a //= b
print(a)
